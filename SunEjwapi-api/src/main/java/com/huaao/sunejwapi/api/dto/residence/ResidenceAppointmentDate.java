package com.huaao.sunejwapi.api.dto.residence;

import com.huaao.sunejwapi.common.web.ApiDemoData;
import com.huaao.sunejwapi.common.web.ApiDescription;

public class ResidenceAppointmentDate {

	@ApiDescription("预约日期")
	@ApiDemoData("2017-05-26")
	private String date;
	
	@ApiDescription("上午时段")
	@ApiDemoData("08:30-10:00,09:00-10:30,09:30-11:00")
	private String amTime;
	
	@ApiDescription("上午时段是否已预约， 0 未预约， 1 已预约")
	@ApiDemoData("0,1,0")
	private String amFlag;
	
	@ApiDescription("下午时段")
	@ApiDemoData("02:30-04:00,03:00-04:30,03:30-05:00")
	private String pmTime;
	
	@ApiDescription("下午时段是否已预约， 0 未预约， 1 已预约")
	@ApiDemoData("0,1,0")
	private String pmFlag;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getAmTime() {
		return amTime;
	}

	public void setAmTime(String amTime) {
		this.amTime = amTime;
	}

	public String getAmFlag() {
		return amFlag;
	}

	public void setAmFlag(String amFlag) {
		this.amFlag = amFlag;
	}

	public String getPmTime() {
		return pmTime;
	}

	public void setPmTime(String pmTime) {
		this.pmTime = pmTime;
	}

	public String getPmFlag() {
		return pmFlag;
	}

	public void setPmFlag(String pmFlag) {
		this.pmFlag = pmFlag;
	}
	
}
